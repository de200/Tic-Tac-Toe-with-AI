package pl.swieczkowski;

public class Coordinates {

    private final int axisX;
    private final int axisY;


    public Coordinates(int axisX, int axisY) {
        this.axisX = axisX;
        this.axisY = axisY;
    }


    public int getAxisX() {
        return axisX;
    }

    public int getAxisY() {
        return axisY;
    }


}
